code = bytearray([
    0xA9, 0xff,
    0x8D, 0x02, 0x60,

    0xA9, 0x55,
    0x8D, 0x00, 0x60,

    0xA9, 0xAA,
    0x8D, 0x00, 0x60,

    0x4C, 0x05, 0x80
])

rom = code + bytearray([0xEA] * (32768 - len(code)))

rom[0x7FFC] = 0x00
rom[0x7FFD] = 0x80

with open("rom.bin", "wb") as out_file:
    out_file.write(rom)
